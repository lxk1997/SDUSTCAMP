package com.clxk.h.sdustcamp.bean;

public class Cet46 {
    private String name;
    private String school;
    private String testlevel;
    private String testid;
    private String grade;
    private String listen;
    private String read;
    private String write;
    private String readlevel;

    public Cet46(String name, String school, String testlevel, String testid, String grade, String listen, String read, String write, String readlevel) {
        this.name = name;
        this.school = school;
        this.testlevel = testlevel;
        this.testid = testid;
        this.grade = grade;
        this.listen = listen;
        this.read = read;
        this.write = write;
        this.readlevel = readlevel;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSchool() {
        return school;
    }

    public void setSchool(String school) {
        this.school = school;
    }

    public String getTestlevel() {
        return testlevel;
    }

    public void setTestlevel(String testlevel) {
        this.testlevel = testlevel;
    }

    public String getTestid() {
        return testid;
    }

    public void setTestid(String testid) {
        this.testid = testid;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getListen() {
        return listen;
    }

    public void setListen(String listen) {
        this.listen = listen;
    }

    public String getRead() {
        return read;
    }

    public void setRead(String read) {
        this.read = read;
    }

    public String getWrite() {
        return write;
    }

    public void setWrite(String write) {
        this.write = write;
    }

    public String getReadlevel() {
        return readlevel;
    }

    public void setReadlevel(String readlevel) {
        this.readlevel = readlevel;
    }
}
